DROP DATABASE NEW_PERSONEL;
CREATE DATABASE NEW_PERSONEL;
USE DATABASE NEW_PERSONEL;
CREATE TABLE DEPT(DEPTNO INT(2) NOT NULL, DNAME VARCHAR(15), LOC VARCHAR (15), PRIMARY KEY(DEPTNO));
CREATE TABLE EMP(EMPNO INT(2) NOT NULL, ENAME VARCHAR(15), JOB VARCHAR(15),
 HIRE DATE, MGR INT(4), SAL FLOAT(7,2), COMM INT(4), DEPTNO INT(2), PRIMARY KEY(EMPNO),
 FOREIGN KEY(DEPTNO) REFERENCES DEPT(DEPTNO));
CREATE TABLE PROJ(PROJ_CODE INT(3) NOT NULL, DESCRIPTION VARCHAR(15), PRIMARY KEY(PROJ_CODE));
CREATE TABLE ASSIGN(EMPNO INT(2) NOT NULL, PROJ_CODE INT(3), A_TIME INT(4),
 PRIMARY KEY(EMPNO, PROJ_CODE), FOREIGN KEY(EMPNO) REFERENCES EMP(EMPNO), FOREIGN KEY(PROJ_CODE)
 REFERENCES PROJ(PROJ_CODE));
